CREATE OR REPLACE FUNCTION FN_GET_GRADE(p_score NUMBER)
RETURN VARCHAR2 
IS
v_grade CHAR(1);
BEGIN
IF p_score >= 4.0 THEN
v_grade := 'A';
ELSIF p_score >= 3.5 THEN
v_grade := 'B';
ELSIF p_score >= 3.0 THEN
v_grade := 'C';
ELSIF p_score >= 2.5 THEN
v_grade := 'D';
ELSE
v_grade := 'F';
END IF;
RETURN v_grade;
END;

SELECT 
  FN_GET_GRADE(4.0),
  FN_GET_GRADE(3.9),
  FN_GET_GRADE(3.6),
  FN_GET_GRADE(3.1),
  FN_GET_GRADE(1.0)
FROM DUAL;

